package 입력_jungol;

public class 입력_연습문제3 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println(a + "+" + b + "=" + (a + b)); //a+ (+는 ,같은 느낌)



        a = 30; //int를 선언해준 다음부터는 int 작성 안해도 됨 다시 선언하면 오류남
        b = 40;

        System.out.println(a + "+" + b + "=" + (a + b));
    }
}
