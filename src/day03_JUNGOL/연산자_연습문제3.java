package day03_JUNGOL;

public class 연산자_연습문제3 {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;

        System.out.println("최초값 a = " + a + ", b = " + b);

        System.out.println("a++ = " + (a++));
        System.out.println("++b = " + (++b));
        System.out.println("실행후 a = " + a + ", b = " + b);

        System.out.println();

        System.out.println("a-- = " + (a--));
        System.out.println("--b = " + (--b));
        System.out.println("실행후 a = " + a + ", b = " + b);
    }
}
